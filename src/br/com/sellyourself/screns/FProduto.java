    private void jbDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarActionPerformed

        if (estado == 2) {

            if (JOptionPane.showConfirmDialog(this, "Você deseja excluir este registro?") == 0) {

                dao.delete(object, new Where(object.getPRIMARYKEY() + " = " + object.getId_produto()));

                controle.cleanView();

                this.Id_produto.setText(UtilDao.selectProxId(object.getTABLE(), object.getPRIMARYKEY()) + "");

                this.estado = 1;

            }

        }

    }//GEN-LAST:event_jbDeletarActionPerformed

    private void jbProcurar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProcurar1ActionPerformed

        GrupoProduto gp = new GrupoProduto();
        GrupoProdutoDao gpDao = new GrupoProdutoDao();

        ConsultaCad cc = new ConsultaCad(null, true, gp, gpDao) {
            @Override
            public void configTable() {

                UtilTable.centerTable(getJtTabela(), 0);

                UtilTable.setSizeColumn(getJtTabela(), 0, 30);
                UtilTable.setSizeColumn(getJtTabela(), 1, 200);

            }
        };
        cc.setSize(400, 300);
        cc.setVisible(true);

        if (cc.getObject() instanceof GrupoProduto) {
            gp = (GrupoProduto) cc.getObject();

            Grupo_id.setText(gp.getId_grupo() + " - " + gp.getDescricao());

        } else {
            System.err.println("Erro ao retornar o Grupo Produto");
        }

    }//GEN-LAST:event_jbProcurar1ActionPerformed

    private void jbProcurar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProcurar2ActionPerformed
        
        int escolha = 0;
        escolha = JOptionPane.showConfirmDialog(this, "Você deseja imprimir uma listagem do cadastro de Produto?");

        if (escolha == 0) {
            RepProduto rPr = new RepProduto();
            Produto pr = new Produto();
            ProdutoDao prDao = new ProdutoDao();

            DRDataSource drDS = prDao.getDataSource(pr);

            rPr.build(drDS);
        }
        
    }//GEN-LAST:event_jbProcurar2ActionPerformed

