    private void jbOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOkActionPerformed

        if (controle.validView()) {

            if (estado == 1) {

                updateControl();
                Grupo_id.setText(UtilGeral.returnIdFromIdDesc(Grupo_id.getText()));
                controle.insert();
                controle.cleanView();

                JOptionPane.showMessageDialog(rootPane, "Produto adicionado com sucesso!");

            } else if (estado == 2) {

                updateControl();
                Grupo_id.setText(UtilGeral.returnIdFromIdDesc(Grupo_id.getText()));
                controle.update();
                controle.cleanView();

                JOptionPane.showMessageDialog(rootPane, "Produto alterado com sucesso!");

                this.estado = 1;

            }

        }

    }//GEN-LAST:event_jbOkActionPerformed

