    private void jbProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProcurarActionPerformed

        ConsultaCad cc = new ConsultaCad(null, true, object, dao) {
            @Override
            public void configTable() {

                UtilTable.centerTable(getJtTabela(), 0);

                UtilTable.setSizeColumn(getJtTabela(), 0, 30);
                UtilTable.setSizeColumn(getJtTabela(), 1, 200);

            }
        };
        cc.setSize(400, 300);
        cc.setVisible(true);

        if (cc.getObject() instanceof Produto && cc.getObject() != null) {
            this.object = (Produto) cc.getObject();

            updateControl();
            this.controle.setView();

            //Busca cadastro auxiliar
            GrupoProdutoDao gpDao = new GrupoProdutoDao();
            GrupoProduto gp = new GrupoProduto();
            ArrayList<GrupoProduto> aGp = gpDao.selectId(gp, Grupo_id.getText());
            if (aGp != null) {
                if (!aGp.isEmpty()) {
                    Grupo_id.setText(Grupo_id.getText() + " - " + aGp.get(0).getDescricao());
                }
            }

            this.estado = 2;
        } else {
            System.err.println("Erro ao retornar o Produto");
            this.object = new Produto();
            init();

            this.estado = 1;
        }

    }//GEN-LAST:event_jbProcurarActionPerformed

