    private void jbDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeletarActionPerformed

        if (estado == 2) {

            if (JOptionPane.showConfirmDialog(this, "Voc√™ deseja excluir este registro?") == 0) {

                dao.delete(object, new Where(object.getPRIMARYKEY() + " = " + object.getId_cliente()));

                controle.cleanView();

                this.Id_cliente.setText(UtilDao.selectProxId(object.getTABLE(), object.getPRIMARYKEY()) + "");

                this.estado = 1;

            }

        }

    }//GEN-LAST:event_jbDeletarActionPerformed

    private void jbProcurar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProcurar1ActionPerformed

        int escolha = 0;
        escolha = JOptionPane.showConfirmDialog(this, "Voc√™ deseja imprimir uma listagem do cadastro de Cliente?");

        if (escolha == 0) {
            RepCliente rGP = new RepCliente();
            Cliente obj = new Cliente();
            ClienteDao gpDao = new ClienteDao();

            DRDataSource drDS = gpDao.getDataSource(obj);

            rGP.build(drDS);
        }


    }//GEN-LAST:event_jbProcurar1ActionPerformed

