    private void jbOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOkActionPerformed

        if (controle.validView()) {

            if (estado == 1) {

                updateControl();
                controle.insert();
                controle.cleanView();

                JOptionPane.showMessageDialog(rootPane, "Cliente adicionado com sucesso!");

            } else if (estado == 2) {

                updateControl();
                controle.update();
                controle.cleanView();

                JOptionPane.showMessageDialog(rootPane, "Cliente alterado com sucesso!");

                this.estado = 1;

            }

        }

    }//GEN-LAST:event_jbOkActionPerformed

    private void jbCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCancelarActionPerformed

        controle.cleanView();

        this.Id_cliente.setText(UtilDao.selectProxId(object.getTABLE(), object.getPRIMARYKEY()) + "");

        this.estado = 1;


    }//GEN-LAST:event_jbCancelarActionPerformed

    private void jbProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbProcurarActionPerformed

        ConsultaCad cc = new ConsultaCad(null, true, object, dao) {
            @Override
            public void configTable() {

                UtilTable.centerTable(getJtTabela(), 0);

                UtilTable.setSizeColumn(getJtTabela(), 0, 30);
                UtilTable.setSizeColumn(getJtTabela(), 1, 200);

            }
        };
        cc.setSize(400, 300);
        cc.setVisible(true);

        if (cc.getObject() instanceof Cliente && cc.getObject() != null) {
            this.object = (Cliente) cc.getObject();

            updateControl();
            this.controle.setView();
            this.estado = 2;
        } else {
            System.err.println("Erro ao retornar o Cliente");
            this.object = new Cliente();
            init();

            this.estado = 1;
        }

    }//GEN-LAST:event_jbProcurarActionPerformed

